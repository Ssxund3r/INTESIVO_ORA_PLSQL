--SIMULANDO ERRO 1
SELECT EMPLOYEE_ID, FIRST_NAME, SALARY 
FROM HR.EMPLOYEES
UNION 
SELECT EMPLOYEE_ID, FIRST_NAME 
FROM HR.EMPLOYEES
ORDER BY 1;

--SIMULANDO ERRO 2
SELECT EMPLOYEE_ID, FIRST_NAME, SALARY 
FROM HR.EMPLOYEES 
UNION 
SELECT EMPLOYEE_ID, SALARY, FIRST_NAME 
FROM HR.EMPLOYEE
ORDER BY 1;

--CAMPOS DIFERENTES MAS COM MESMO TIPO -> ATRIBUTO EX.: VARCHAR2 = VARCHAR2
SELECT EMPLOYEE_ID, FIRST_NAME, SALARY 
FROM HR.EMPLOYEES
UNION 
SELECT EMPLOYEE_ID, LAST_NAME, SALARY 
FROM HR.EMPLOYEES
ORDER BY 1;

