--EXEMPLO USANDO UNION 
SELECT ANO, ESTADO
FROM SENSO
WHERE ANO = '2014'
UNION 
SELECT ANO, ESTADO
FROM SENSO
WHERE ANO ='2013'
ORDER BY ESTADO DESC;

--EXEMPLO USANDO UNION ALL
SELECT ANO, ESTADO
FROM SENSO
WHERE ANO = '2014'
UNION ALL
SELECT ANO, ESTADO
FROM SENSO
WHERE ANO ='2013'
ORDER BY ESTADO DESC;

-- EXEMPLO COM UNION 
SELECT ANO, ESTADO, NOME_MUN, POPULACAO
FROM SENSO 
WHERE ANO = '2014' 
AND COD_UF = '11'
UNION 
SELECT ANO, ESTADO, NOME_MUN, POPULACAO
FROM SENSO
WHERE ANO ='2013'
AND COD_UF = '11'
ORDER BY NOME_MUN, ANO DESC;